package problems

import "math"

func FindSmallestIntInMap(m map[int]int) int {
	// Эта функция находит наименьшее значение в мапе целых чисел.
	//
	// Аргумент:
	//   - m map[int]int: мапа, где ключи и значения являются целыми числами
	//   - например: map[int]int{1: 10, 2: 5, 3: 15}
	//
	// Алгоритм:
	//   - Придумай самостоятельно, используя логику похожую на поиск минимального числа в слайсе
	//
	// Что может понадобиться:
	//   - Для прохода по мапе используй цикл for key, value := range m { ... }
	//   - Обрати внимание, что нас интересуют значения (value), а не ключи (key)
	//   - Не забудь про случай пустой мапы
	//
	// Пример для мапы map[int]int{1: 10, 2: 5, 3: 15}:
	//   - Возвращаем 5, так как это наименьшее значение в мапе
	if len(m) == 0 {
		return 0
	}
	var minval int = math.MaxInt
	for _, val := range m {
		if val < minval {
			minval = val
		}
	}
	return minval
}
